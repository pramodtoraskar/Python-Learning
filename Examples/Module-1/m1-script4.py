""" This is python script: Explain python Arithmetic operators !"""

# Examples of Arithmetic Operator
num_1 = 9
num_2 = 4

# Addition of numbers
add_num = num_1 + num_2

# Subtraction of numbers
sub_num = num_1 - num_2

# Multiplication of number
mul_num = num_1 * num_2

# Division(float) of number
div1_num = num_1 / num_2

# Division(floor) of number
div2_num = num_1 // num_2

# Modulo of both number
mod_num = num_1 % num_2

# print results
print(add_num)
print(sub_num)
print(mul_num)
print(div1_num)
print(div2_num)
print(mod_num)
