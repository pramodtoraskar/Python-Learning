# Examples of Special Assignment
num_1 = 3
num_2 = 3

str_1 = 'PythonBasic'
str_2 = 'PythonBasic'

list_1 = [1, 2, 3]
list_2 = [1, 2, 3]

print(num_1 is not num_2)

print(str_1 is str_2)

# Output is False, since lists are mutable.
print(list_1 is list_2)
