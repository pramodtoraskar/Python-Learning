""" This is python script: Explain decision making or Conditional Statement!"""

# Python program to demonstrate decision making

num_1 = 10
num_2 = 15

# if to check even number
if num_1 % 2 == 0:
    print("Even Number")

# if-else to check even or odd
if num_2 % 2 == 0:
    print("Even Number")
else:
    print("Odd Number")
