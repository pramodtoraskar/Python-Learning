""" This is python script: Explain python Relational Operators!"""

# Examples of Relational Operators
num_1 = 13
num_2 = 33

# num_1 > num_2 is False
print(num_1 > num_2)

# num_1 < num_2 is True
print(num_1 < num_2)

# num_1 == num_2 is False
print(num_1 == num_2)

# num_1 != num_2 is True
print(num_1 != num_2)

# num_1 >= num_2 is False
print(num_1 >= num_2)

# num_1 <= num_2 is True
print(num_1 <= num_2)